def resolve_city(city: str) -> dict:
    """برگرداندن اطلاعات شهر — اگه پیدا نشد، همون ورودی رو برمی‌گردونه"""
    city = city.strip()
    if city in CITY_MAP:
        return CITY_MAP[city]
    # جستجوی جزئی
    for k, v in CITY_MAP.items():
        if city in k or k in city:
            return v
    return {'fa': city, 'en': city, 'lat': 35.6892, 'lng': 51.3890}
